#include "libft.h"

int	ft_isalnum(int symb)
{
	if ((symb >= 'a' && symb <= 'z') || (symb >= 'A' && symb <= 'Z')
		|| (symb >= '0' && symb <= '9'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
