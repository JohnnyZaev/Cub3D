void draw_line(void *mlx, void *win, int beginX, int beginY, int endX, int endY, int color)
{
	double deltaX = endX - beginX;
	double deltaY = endY - beginY;
	int pixels = sqrt((deltaX * deltaX) + (deltaY * deltaY));

	deltaX /= pixels; // 1
	deltaY /= pixels; // 0

	double pixelX = beginX;
	double pixelY = beginY;
	while (pixels)
	{
		mlx_pixel_put(mlx, win, pixelX, pixelY, color);
		pixelX += deltaX;
		pixelY += deltaY;
		--pixels;
	}
}

int	errors(int err)
{
	if (err == 1)
		printf("Malloc error\n");
	if (err == 2)
		printf("Mlx error\n");
	if (err == 3)
		printf("Arguments error\n");
	exit(err);
}

int	main(int argc, char **argv)
{
	void	*mlx;
	void	*win;
	int		size_x;
	int		size_y;

	(void)argv;
	if (argc != 2)
		errors(3);
	mlx = mlx_init();
	if (!mlx)
		errors(2);
	mlx_get_screen_size(&size_x, &size_y);
	win = mlx_new_window(mlx, size_x, size_y, "cub3D");
	draw_line(mlx, win, 0, 0, 200, 200, 0xFFFFFF);
	draw_line(mlx, win, 0, 70, 340, 200, 0xFFAFAF);
	mlx_loop(mlx);
	return (0);
}
